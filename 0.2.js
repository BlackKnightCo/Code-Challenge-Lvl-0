// Pseudocoe Conversion
x = 1 + 1 * 2 // Multiplicattion goes first here
console.log(x);
y = (1 + 1) * 2 // Brackets go first here
console.log(y); 
z = 1 + ( 1 * 2 )  // Brackets & Multiplication go first here
console.log(z);
a = 1 + 1 * 2 / 2  // Division then  Multiplication go first here
console.log(a);
b = (1 + 1 * 2 ) /  2 // Brackets Division then  Multiplication go first here
console.log(b);